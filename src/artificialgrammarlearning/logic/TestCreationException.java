package artificialgrammarlearning.logic;

public class TestCreationException extends Exception {
	
	private static final long serialVersionUID = -1327535201943041697L;

	public TestCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public TestCreationException(String message) {
		super(message);
	}

	public TestCreationException() {
		super();
	}

	public TestCreationException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public TestCreationException(Throwable arg0) {
		super(arg0);
	}
}
